package com.inqoo.loops;

public class WhileLoop2 {

    public static void main(String[] args) {
        int counter = 0;

        while(counter < 10) {
            System.out.println("Nadal w pętli, licznik: " + counter);
            counter++;
        }
        System.out.println("Koniec pętli");
    }
}
